class Allergies extends React.Component
{
    render()
    {
        if(!this.props.data)
            return;

        var tables=[];

        for(var tableNum=0; tableNum<this.props.data.length; tableNum++)
        {
            var table=this.props.data[tableNum];
            var elements=[];
            var rows=table.rows;
            var headers=table.headers;

            debugvar = table;
            for(var r=0; r<rows.length; r++)
            {
                if(!elements[r])
                    elements[r]=[];

                for(var i=0; i<headers.length; i++)
                {
                    let text = rows[r][i];
                    var listText = [];

                    //Less rows than headers *CHECK*
                    if(text)
                        for(var t=0; t<text.length; t++)
                           listText.push(<p key={r+""+i+""+t}>{text[t].text}</p>);
                    else
                        break;

                    if(text.length == 1)
                        elements[r].push(
                            <dl key={r+""+i} className="dl-horizontal">
                                <dt><span className="label label-default">{headers[i]}</span></dt>
                                <dd className="text-left">{listText}</dd>
                            </dl>
                        );
                    else
                        elements[r].push(
                            <dl key={r+""+i} className="dl-horizontal">
                                {listText}
                            </dl>
                        );
                }
            }

            var listItems=(elements.map(function(element, index)
            {
                return(
                    <div key={index}>
                        {element}
                        <hr/>
                    </div>
                );
            }));

            tables.push(
                <div key={tableNum}>
                    {listItems}
                </div>
            );
        }

        return(
            <div className="col-lg-4 col-md-4 col-sm-4 mb">
                <div className="grey-panel pn">
                    <div className="grey-header">
                        <h4>{this.props.title}</h4>
                    </div>
                    <span className="pn-bg fa fa-heartbeat fa-5x"></span>
                    {tables}
                </div>
            </div>
        );
    }
}

class CollapsiblePanel extends React.Component
{
    render()
    {
        if(!this.props.data)
            return;

        var tables=[];
        var collapsePanelHeading = [];
        var titlePanel = this.props.title;
        for(var tableNum=0; tableNum<this.props.data.length; tableNum++)
        {
            var table=this.props.data[tableNum];
            var elements=[];
            var rows=table.rows;
            var headers=table.headers;

            for(var r=0; r<rows.length; r++)
            {
                if(!elements[r])
                    elements[r]=[];

                for(var i=0; i<headers.length; i++) {
                    let text = rows[r][i];
                    var listText = [];

                    if(text)
                        for(var t=0; t<text.length; t++)
                           listText.push(<p key={r+""+i+""+t}>{text[t].text}</p>);

                    if (i===0) {
                      collapsePanelHeading.push(text[0].text);
                    }
                    elements[r].push(
                        <dl key={r+""+i} className="dl-horizontal">
                            <dt><span className="label label-default">{headers[i]}</span></dt>
                            <dd className="text-left">{listText}</dd>
                        </dl>
                    );
                }
            }

            var listItems=(elements.map(function(element, index)
            {
                var finalStr = collapsePanelHeading[index].replace(/ |\u002E/g,"_");
                var where = finalStr.indexOf("/");
                if (where > 0)
                  finalStr = finalStr.substring(0,where);
                var indexRef = "#"+finalStr+index;
                var indexRefNo = finalStr+index;

                return(
                      <span>
                      <div className="panel-heading grey-header-no-margin">
                        <h4 className="panel-title">
                          <a href={indexRef} data-parent="accordion" data-toggle="collapse">{collapsePanelHeading[index]}
                          </a>
                        </h4>
                      </div>
                      <div id={indexRefNo} className="panel-collapse collapse white-back" key={index}>
                          {element}
                          <hr/>
                      </div>
                      </span>
                );
            }));

            tables.push(
                <div className="panel-default" key={tableNum}>
                    {listItems}
                </div>
            );
        }

        return(
            <div className="col-lg-4 col-md-4 col-sm-4 mb">
                <div className="grey-panel">
                  <div className="grey-header"><h4>{this.props.title}</h4>
                  </div>
                  <span className="pn-bg fa fa-heartbeat fa-5x"></span>
                  <div className="panel-body">
                    <div id="accordion" className="panel-group">
                      {tables}
                    </div>
                  </div>
                </div>
            </div>
        );
    }
}


